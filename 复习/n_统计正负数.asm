DATAS SEGMENT
    DA1 DB 3,4,-5,6,-7,0,0,-4 ,-7
    COUNT EQU $-DA1
    Z DB 0
    ZERO DB 'THE NUMBER OF ZERO:$'
    P DB 0
    PLUS DB 'THE NUMBER OF PLUS:$'
    M DB 0
    MINUS DB 'THE NUMBER OF MINUS:$'
    CRLF DB 0AH,0DH,'$'
DATAS ENDS

STACKS SEGMENT
    ;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
	LEA SI,DA1;第一个数
	MOV CX,COUNT
	
COMPARE:
	MOV AL,[SI]
	CMP AL,0
	JZ ZZ;Z+1
	JG PP;P+1
	JL MM;M+1
	
	
ZZ:
	INC Z
	INC SI
	LOOP COMPARE
	CALL OUTT
PP:
	INC P
	INC SI
	LOOP COMPARE
	CALL OUTT
MM:
	INC M
    INC SI
	LOOP COMPARE
	CALL OUTT
	
OUTT:
	LEA DX,ZERO
	MOV AH,09H
	INT 21H
	ADD Z,30H
	MOV DL,Z
	MOV AH,02H
	INT 21H
	
    LEA DX,CRLF
    MOV AH,09H
    INT 21h

    LEA DX,PLUS
    INT 21h
    ADD P,30h
    MOV DL,P
    MOV AH,02h
    INT 21h

    LEA DX,CRLF
    MOV AH,09h
    INT 21h

    LEA DX,MINUS
    INT 21h
    ADD M,30h
    MOV DL,M
    MOV AH,02h
    INT 21H
	
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START