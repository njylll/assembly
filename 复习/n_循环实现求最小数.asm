DATAS SEGMENT
    SHU DB  02H,69H,6BH,45H,6CH,42H,68H  
    COUNT EQU $-SHU;个数
    S DB 'THE MIN IS: $'
DATAS ENDS

STACKS SEGMENT
    ;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    LEA DX,S
    MOV AH,09H
    INT 21H
    
    MOV CX,COUNT-1
 	LEA SI,SHU
    MOV AL,[SI];第一个数
COMPARE:
    CMP AL,[SI+1]
    JBE CONTINUE
    XCHG AL,[SI+1]
    
CONTINUE:
	INC SI
    LOOP COMPARE
    
	CMP AL,09H
	JBE OUT1
	CMP AL,0FH
	JBE OUT2
	MOV DL,AL
	MOV AH,02H
	INT 21H
	MOV AH,4CH
	INT 21H
OUT1:
	ADD AL,30H
	MOV DL,AL
	MOV AH,02H
	INT 21H
	call exit	
OUT2:
	ADD AL,37H
	MOV DL,AL
	MOV AH,02H
	INT 21H
    call exit
exit:
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START